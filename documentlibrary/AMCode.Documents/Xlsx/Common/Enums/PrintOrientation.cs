namespace AMCode.Documents.Xlsx
{
    /// <summary>
    /// Specifies the page orientation for printing
    /// </summary>
    public enum PrintOrientation
    {
        /// <summary>
        /// Portrait orientation (taller than wide)
        /// </summary>
        Portrait = 0,

        /// <summary>
        /// Landscape orientation (wider than tall)
        /// </summary>
        Landscape = 1
    }
}
