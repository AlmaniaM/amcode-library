namespace AMCode.Documents.Xlsx
{
    /// <summary>
    /// Specifies the position of summary columns in grouped data
    /// </summary>
    public enum SummaryColumnPosition
    {
        /// <summary>
        /// Summary columns appear to the right of detail columns
        /// </summary>
        Right = 0,

        /// <summary>
        /// Summary columns appear to the left of detail columns
        /// </summary>
        Left = 1
    }
}
