namespace AMCode.Documents.Xlsx
{
    /// <summary>
    /// Specifies the position of summary rows in grouped data
    /// </summary>
    public enum SummaryRowPosition
    {
        /// <summary>
        /// Summary rows appear below detail rows
        /// </summary>
        Below = 0,

        /// <summary>
        /// Summary rows appear above detail rows
        /// </summary>
        Above = 1
    }
}
